//Variables 3 formas de declarar variables
// 1. var (forma antigua de declarar variables)
// var es la forma antigua de declarar variables en JavaScript

var nombre = "Gilberto"
console.log(nombre)

nombre = "Gil"
console.log(nombre)

// 2. let (nueva forma de declarar variables)
// let es la forma moderna de declarar variables en JavaScript

let apellido = "Morales"
console.log(apellido)

apellido = "Mora"
console.log(apellido)

// 3. const (constante)
// Una vez que asignamos un valor a una constante, no podemos cambiar ese valor

const PI = 3.1416
console.log(PI)


